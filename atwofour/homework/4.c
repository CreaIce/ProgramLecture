#include <stdio.h>

int main(void)
{
    int p[10];
    int i;
    int j = 0;
    int k = 0;
    int m = 0;

    printf("10人をグループ1~3に分けるべし！(入力 1~3)\n");

    for (i = 0; i < 10; ++i)
    {
        scanf("%d",&p[i]);
    }
    
    for (i = 0; i < 10; ++i)
    {
        if (p[i] == 1)
        {
            j += p[i];
        }
    }
    
    printf("グループ1　%d人\n",j);
    
    for (i = 0; i < 10; ++i)
    {
        if (p[i] == 2)
        {
            k += p[i];
        }
    }
    int l = k/2;

    printf("グループ2　%d人\n",l);

    for (i = 0; i < 10; ++i)
    {
        if (p[i] == 3)
        {
            m += p[i];
        }
    }
    int n = m/3;

    printf("グループ3　%d人\n",n);

    return 0;
}

